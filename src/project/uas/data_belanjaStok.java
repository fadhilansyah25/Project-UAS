    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        // TODO add your handling code here:
        int ok_FadilArdiansyah = JOptionPane.showConfirmDialog(null, "Apakah ingin mengedit Data ini?", "Edit Data", JOptionPane.YES_NO_CANCEL_OPTION);
        try {
            PreparedStatement stat_FadilArdiansyah = conn_FadilArdiansyah.prepareStatement(
                "update belanja_stok set id_belanja=?, nama_supplier=?, id_barang=?, jumlah_belanja=? where id_belanja='"
                +Integer.parseInt(id_belanjaTextField.getText())+"'");
                if (ok_FadilArdiansyah == 0) {
                    try {
                        stat_FadilArdiansyah.setInt(1, Integer.parseInt(id_belanjaTextField.getText()));
                        stat_FadilArdiansyah.setString(2, nama_supplierTextField.getText());
                        stat_FadilArdiansyah.setString(3, (String)id_barangComboBox.getSelectedItem());
                        stat_FadilArdiansyah.setInt(4, Integer.parseInt(jumlah_belanjaTextField.getText()));
                        stat_FadilArdiansyah.executeUpdate();

                        JOptionPane.showMessageDialog(null, "Update Data Berhasil");
                    } catch (Exception e) {
                        //TODO: handle exception
                        JOptionPane.showMessageDialog(null, "Update Data Gagal"+e);
                    }
                }
            tabel();
        } catch (Exception e) {
            //TODO: handle exception
        }
    }//GEN-LAST:event_editButtonActionPerformed

    private void hapusButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusButtonActionPerformed
        // TODO add your handling code here:
        int ok_FadilArdiansyah = JOptionPane.showConfirmDialog(null, "Apakah Yakin Menghapus Data?", "Hapus Data", JOptionPane.YES_NO_CANCEL_OPTION);
        if (ok_FadilArdiansyah == 0) {
            try {
                String sql_FadilArdiansyah = "delete from belanja_stok where id_belanja="+Integer.parseInt(id_belanjaTextField.getText());
                String resetno =  "ALTER TABLE belanja_stok DROP id_belanja";
                String consecutivenumbers = "ALTER TABLE belanja_stok ADD  id_belanja INT( 3 ) NOT NULL AUTO_INCREMENT FIRST ,ADD KEY (id_belanja)";
                PreparedStatement stat_FadilArdiansyah = conn_FadilArdiansyah.prepareStatement(sql_FadilArdiansyah);
                stat_FadilArdiansyah.executeUpdate();
                conn_FadilArdiansyah.createStatement().execute(resetno);
                conn_FadilArdiansyah.createStatement().execute(consecutivenumbers);
                JOptionPane.showMessageDialog(null, "Hapus Data Berhasil");
            } catch (Exception e) {
                //TODO: handle exception
                JOptionPane.showMessageDialog(null, "Hapus Data Gagal"+e);
            }
        }
        tabel();
    }//GEN-LAST:event_hapusButtonActionPerformed

