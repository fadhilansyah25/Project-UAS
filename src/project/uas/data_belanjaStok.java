    private void kosongkan() {
        id_belanjaTextField.setText("");
        nama_supplierTextField.setText("");
        id_barangComboBox.setSelectedIndex(-1);
        nama_barangTextField.setText("");
        jumlah_belanjaTextField.setText("");
    }

    private void tabel(){
        DefaultTableModel tabel = new DefaultTableModel();
        tabel.addColumn("ID Belanja");
        tabel.addColumn("Nama Supplier");
        tabel.addColumn("ID Barang");
        tabel.addColumn("Nama Barang");
        tabel.addColumn("Jumlah Belanja");
        Tabel_Barang.setModel(tabel);

        try {
            res_FadilArdiansyah = stat_FadilArdiansyah.executeQuery("SELECT id_belanja, nama_supplier, jumlah_belanja, tabel_databarang.id_barang, tabel_databarang.nama_barang FROM belanja_stok INNER JOIN tabel_databarang ON belanja_stok.id_barang = tabel_databarang.id_barang");
            while (res_FadilArdiansyah.next()) {
                tabel.addRow(new Object[]
                {
                    res_FadilArdiansyah.getString("id_belanja"),
                    res_FadilArdiansyah.getString("nama_supplier"),
                    res_FadilArdiansyah.getString("tabel_databarang.id_barang"),
                    res_FadilArdiansyah.getString("tabel_databarang.nama_barang"),
                    res_FadilArdiansyah.getString("jumlah_belanja")
                });
            }
        } catch (Exception e) {
            //TODO: handle exception
            JOptionPane.showMessageDialog(null, e);
        }
    }

    private void tampil_combobox() {
        try {
            String sql = "select id_barang from tabel_databarang order by id_barang asc";
            res_FadilArdiansyah = stat_FadilArdiansyah.executeQuery(sql);
            id_barangComboBox.addItem("-Pilih Kode Barang");
            while (res_FadilArdiansyah.next()) {
                id_barangComboBox.addItem(res_FadilArdiansyah.getString("id_barang"));
                
            }
        } catch (Exception e) {
            //TODO: handle exception
            JOptionPane.showMessageDialog(null, e);
        }
    }

    private void tampil() {
        try {
            String sql = "select nama_barang from tabel_databarang where id_barang='"+id_barangComboBox.getSelectedItem()+"'";
            res_FadilArdiansyah = stat_FadilArdiansyah.executeQuery(sql);

            while (res_FadilArdiansyah.next()) {
                {   
                    nama_barangTextField.setText(res_FadilArdiansyah.getString("nama_barang"));
                }
            }
        } catch (Exception e) {
            //TODO: handle exception
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }
